let n=23;
// odd or even
if(n%2==0){
    console.log("even")
}
else{
    console.log("Odd")
}
//positive /negative / zero
if(n>0){
    console.log("Possitive Number");
}
else if(n<0){
    console.log("Negative Number");
}
else{
    console.log("Zero");
}

//divisible by both 3 and 5
